const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const exampleValidation = require('../../validations/example.validation');
const exampleController = require('../../controllers/example.controller');

const router = express.Router();

router
  .route('/')
  .post(auth(), validate(exampleValidation.createExample), exampleController.createExample)
  .get(auth(), validate(exampleValidation.getExamples), exampleController.getAllExamples);

router
  .route('/:id')
  .get(auth(), validate(exampleValidation.getExample), exampleController.getExample)
  .patch(auth(), validate(exampleValidation.updateExample), exampleController.updateExample)
  .delete(auth(), validate(exampleValidation.deleteExample), exampleController.deleteExample);
module.exports = router;

